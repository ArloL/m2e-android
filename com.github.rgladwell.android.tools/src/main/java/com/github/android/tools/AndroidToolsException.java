package com.github.android.tools;

public class AndroidToolsException extends Exception {

    private static final long serialVersionUID = 290554973009770654L;

	public AndroidToolsException() {
	    super();
    }

	public AndroidToolsException(String message, Throwable cause) {
	    super(message, cause);
    }

	public AndroidToolsException(String message) {
	    super(message);
    }

	public AndroidToolsException(Throwable cause) {
	    super(cause);
    }

}
